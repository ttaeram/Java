package variable;

public class Var5 {
    public static void main(String[] args) {
        int a = 100;
        double b = 100.5;
        boolean c = true;
        char d = 'A';
        String e = "String e";

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
